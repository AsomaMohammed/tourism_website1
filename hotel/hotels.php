// دالة لعرض النجوم
function renderStars($rating, $interactive = false) {
    $stars = '';
    for ($i = 1; $i <= 5; $i++) {
        $filled = $i <= $rating ? 'filled' : '';
        $dataValue = $interactive ? "data-value=\"$i\"" : '';
        $stars .= "<span class=\"star $filled\" $dataValue>★</span>";
    }
    return $stars;
}
